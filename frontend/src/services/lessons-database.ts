/**
 * Получить урок по ID
 */
export const getLessonById = (id: string): AstroLesson | undefined => {
  return ASTRO_LESSONS.find((lesson) => lesson.id === id);
};

/**
 * Получить уроки по категории
 */
export const getLessonsByCategory = (
  category: LessonCategory
): AstroLesson[] => {
  return ASTRO_LESSONS.filter((lesson) => lesson.category === category).sort(
    (a, b) => a.order - b.order
  );
};

/**
 * Получить все категории с количеством уроков
 */
export const getCategoriesWithCount = () => {
  const categories: Record<LessonCategory, number> = {
    basics: 0,
    planets: 0,
    signs: 0,
    houses: 0,
    aspects: 0,
    transits: 0,
    practical: 0,
    lunar: 0,
  };

  ASTRO_LESSONS.forEach((lesson) => {
    categories[lesson.category]++;
  });

  return categories;
};

/**
 * Получить связанные уроки
 */
export const getRelatedLessons = (lessonId: string): AstroLesson[] => {
  const lesson = getLessonById(lessonId);
  if (!lesson || !lesson.relatedLessons) return [];

  return lesson.relatedLessons
    .map((id: string) => getLessonById(id))
    .filter((lesson): lesson is AstroLesson => lesson !== undefined);
};

/**
 * Поиск уроков по тексту
 */
export const searchLessons = (query: string): AstroLesson[] => {
  const lowercaseQuery = query.toLowerCase();
  return ASTRO_LESSONS.filter(
    (lesson) =>
      lesson.title.toLowerCase().includes(lowercaseQuery) ||
      lesson.shortText.toLowerCase().includes(lowercaseQuery) ||
      lesson.category.toLowerCase().includes(lowercaseQuery)
  );
};
